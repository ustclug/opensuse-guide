<div class="heading1">6. 常用软件</div>
这里将介绍一些日常工作中的常用软件，这些软件都是默认安装的并且质量很好，当然您也可以选择其他您想要的软件。<br /><br />

<div class="tip">
<table>
<tbody>
<tr>

<td><img src="images/pics/tip.png" alt="tip" /></td>
<td>
所有下文提及的可选软件均可用<a href="installpackage.php">软件包管理器</a>来简单安装后使用，如果其需要的<a href="repositories.php">软件源</a>已添加的话（请看后面章节）。 
</td>
</tr>
</tbody>
</table>
</div><br />

您可以在这找到更多您喜好的应用的替代软件：
<br />
<a href="http://en.opensuse.org/Application_equivalents" target="_blank">http://en.opensuse.org/Application_equivalents</a><br /><br />

openSUSE 默认使用Fcitx中文输入法，候选字翻页使用减号 - 和加号 + 按键。
<br /><br />

<div class="heading2">6.1 使用Firefox网上冲浪</div>
Mozilla Firefox 是 openSUSE 下的默认网络浏览器。它是最主流的浏览器之一。
（如果在开启 Kwin 3D特效后 Firefox 变得很卡，则可在 Firefox 中将 “平滑滚动” 的功能去掉，具体在 选项-高级-普通-浏览 下。）<br />
<br />


<center><a href="images/screenshots/firefox.png" rel="thumbnail"><img src="images/screenshots/firefoxb.png" alt="firefox" class="pic" /></a></center><br />

其他可选的：Opera, 
<a href="http://www.google.com/chrome?platform=linux" target="_blank">
Chrome
</a>
, Chromium, Falkon, Konqueror<br /><br />

<div class="heading2">6.2 使用LibreOffice进行日常办公</div>
LibreOffice 是一个完整的 Office 办公套装，包括文字处理、表格处理、幻灯片、画图以及其他套件，并与微软的 Office 良好兼容。
<br />
<br />

<center><a href="images/screenshots/lowriter.png" rel="thumbnail"><img src="images/screenshots/lowriterb.png" alt="libreoffice" class="pic" /></a></center><br />

其他可选的：KOffice, Abiword, Gnumeric 和永中Office<br /><br />
<b>
本站力荐国产的WPS办公套件：<br />
<a href="http://linux.wps.cn/" target="_blank">
WPS For Linux
</a>
</b>

<br /><br />


<div class="heading2">6.3 使用Amarok听音乐</div>
Amarok 是一个功能强大的音乐播放器，并拥有很完善的音乐管理功能。
不过在能播放音乐之前，<b>需要先安装解码器</b>，这将会在后面的章节中介绍。<br /><br />


<center><a href="images/screenshots/amarok.png" rel="thumbnail"><img src="images/screenshots/amarokb.png" alt="amarok" class="pic" /></a></center><br />

其他可选的：Elisa, Clementine, JuK, Qmmp和Audacious，Tomahawk，
<a href="https://sourceforge.net/projects/iceplayer/" target="_blank">
iceplayer
</a>
。<br />
中文歌词显示和自动下载歌词及桌面歌词秀推荐使用：<br />
三翻领的
<a href="http://kde-apps.org/content/show.php/lrcShow-X?content=103055>" target="_blank">lrcShow-X
</a>或者<br />
tigersoldier和xinzhi.org、simplyzhao做的
<a href="https://github.com/tigersoldier/osd-lyrics" target="_blank">
osd-lyrics
</a>
<br /><br />

<div class="heading2">6.4 使用 VLC 或 Dragon 播放器看视频</div>
默认安装了 VLC 播放器，用于播放电影和DVD等视频，当然 Dragon 播放器也可以。后面章节中会介绍如何安装解码器。
安装解码器时可以一起安装 MPlayer、SMPlayer。<br /><br />

<center><a href="images/screenshots/vlc.png" rel="thumbnail"><img src="images/screenshots/vlcb.png" alt="VLC" class="pic" /></a></center><br />

其他可选的：MPlayer，SMPlayer, 
<a href="http://www.videolan.org/vlc/download-suse.html"  target="_blank">
VLC官网版本</a>
, UMPlayer, mpv, Kaffeine<br /><br />


<div class="heading2">6.5 使用GIMP编辑图片</div>
GIMP (GNU Image Manipulation Program，GNU图像处理程序) 是一个非常强大的图片编辑软件。<br /><br />


<center><a href="images/screenshots/gimp.png" rel="thumbnail"><img src="images/screenshots/gimpb.png" alt="gimp" class="pic" /></a></center><br />

