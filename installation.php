<div class="os2">4.3 升级安装</div>
如果原来有低版本的openSUSE Linux，可以选择在线升级安装。
openSUSE官方有教程：<br />
<a href="http://en.opensuse.org/Upgrade" target="_blank">
<b>http://en.opensuse.org/Upgrade</b>
</a> <br />
从旧的系统升级到新的，看文中第4节：Running the Upgrade。<br />
升级时，如果加入了Nvidia或Ati的显卡驱动源，以及Packman源，
要特别小心，源的地址一定要正确，所选的镜像站也要靠谱，否则可能导致升级失败。<br /><br />
加入第三方源，尤其是不靠谱的第三方源，可能导致升级失败。如果升级失败，
那么很可能图形界面Yast被破坏，不能使用，这时不要惊慌。打开命令行，
使用zypper命令重新配置靠谱的安装源，然后运行zypper ref，
接着运行zypper dup。通过命令行可以继续完成升级，当然，安装源要靠谱，
否则还可能挂。<br /><br />
使用zppper命令配置安装源，请看后面第11章。

