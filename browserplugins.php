<div class="heading1">14. 浏览器插件</div>

很多网站要求安装各种浏览器插件以使其达到预期的效果。这里是一些您可能想要安装的插件。这些插件安装方法对 GNOME 和 KDE 桌面都通用。 命令行安装需要root管理员权限，输入su，按提示输入密码，然后以管理员身份安装软件。其他安装方式也要按提示输入密码。
<br /><br /> 

