<div class="heading1">12. 与 Windows 世界兼容</div>
不得不承认，个人电脑桌面世界被微软主宰着，他们并不很清楚如何与其他系统轻松地协同工作。尽管如此，在大多数情况下是可能实现相当无缝的协同工作的。本章讨论最常见的兼容问题。
<br /><br />


<div class="heading2">12.1 Office 文档</div>
LibreOffice 默认的开放文档格式(Open Document Format: *.odt, *.ods, *.odp, etc.) 是一个开放的标准格式，微软Microsoft Office  2007 SP2以上版本能部分支持该格式。或者您可以在您的MS Windows和Mac OSX安装LibreOffice，其对这些平台当然也是自由可用的。<br /><br />

LibreOffice也能很好地阅读和写入Microsoft Office格式（*.doc, *.xls,*.ppt, *.docx, *.xlsx, *.pptx 等）——以及其他各种格式。
<br /><br />

<div class="tip">
<table>
<tbody>
<tr>
<td><img src="images/pics/tip.png" alt="tip" /></td>
<td>
如果您遇到LibreOffice不能很好解析的微软格式文档，那么您可以考虑尝试Calligra办公套件（前身是KOffice）会不会好点。
</td>
</tr>
</tbody>
</table>
</div><br />

<div class="heading3">12.1.1 字体</div>
默认已经安装了自由的Liberation Fonts，中文的是文泉驿字体，提供的字体效果和
微软的字体相当。如果您需要常规的微软字体，安装软件包 <b>fetchmsttfonts</b> 。<br />
如果安装了双系统，可以自行从微软Windows字体文件夹里导入字体：
注销普通用户，用root用户登录；
比如Windows的C盘加载位置是 /windows/C ，那么用文件管理器打开
 /windows/C/WINDOWS/Fonts 目录，选中全部字体文件，右击文件=>Actions=>Install，
 然后就可以安装字体了，有些字体会被提醒说已经有了，跳过去就行了。
<br /><br />

<div class="heading2">12.2 "Windows网上邻居"</div>
可以使用 Samba 服务加入本地网络与装有 MS Windows 系统的机器进行文件共享。<br /><br />


<div class="heading3">12.2.1 访问网络共享</div>
访问其他机器上的共享文件并不需要配置，只需要简单的操作一下： 
<div class="path">打开 Dolphin 文件管理器 => 单击地址栏或者使用快捷键 Ctrl+L => 输入 ' smb://[IP地址] '</div><br />

<center><a href="images/screenshots/smb-dolph.png" rel="thumbnail"><img src="images/screenshots/smb-dolphb.png" alt="smb-dolphin" class="pic" /></a></center><br />

如果您不知道 ip 地址，可以先在 Dolphin 地址栏中输入 <i>smb:/</i> 来浏览整个本地网络，但是先要配置好防火墙。<br />
<div class="path">YaST =&gt; 安全和用户  =&gt; 防火墙 =&gt; 允许的服务 =&gt; 从“要允许的服务”组合框里选择 "Samba Client" 和 "Netbios Server" 并添加它们到允许的服务列表。</div><br />

<center><a href="images/screenshots/firewall-samba.png" rel="thumbnail"><img src="images/screenshots/firewall-sambab.png" alt="firewall-samba" class="pic" /></a></center><br />




<br /><br />


<div class="heading3">12.2.2 共享</div>
如果需要向局域网的 MS Windows 用户、Mac OSX 用户或者其他 GNU/Linux 用户共享 <i>您的</i> 文件的话，则需要配置 Samba服务器（确保 yast2-samba-server 和 samba 软件包已经安装）： <br /><br />
<b>1) </b><br />
打开 Yast 的 Samba服务器模块。<br />
<div class="path">YaST =&gt; Network Services（网络服务）  =&gt; Samba Server（Samba服务器）</div><br />


<center><a href="images/screenshots/samba-server.png" rel="thumbnail"><img src="images/screenshots/samba-serverb.png" alt="samba" class="pic" /></a></center><br />

<br /><br />

<b>2) </b><br />
在 <i>Start-Up</i> （启动）页面您可以选择是否让 Samba 服务开机自动启动，
和是否打开所需的防火墙端口。
<br /><br />

<b>3) </b><br />
到 <i>Shares</i> （共享）页面，选中<i>Allow Users to Share Their Directories</i>（允许用户共享目录） 和 <i>Allow Guest Access</i> （允许Guest访问）两个选项。在 <i>Identity</i> （标识）页面您可以设置您的工作组和共享名。
<br /><br />

<b>4) </b><br />
点击 "添加" 按钮，指定您希望共享的文件夹即可。
<br /><br />


<div class="heading2">12.3 运行 MS Windows 程序</div>

几乎所有的应用都存在其高质量的原生的 GNU/Linux 程序。但如果您依赖一些MS Windows专用程序的话，在GNU/Linux下安装它们来使用是可能成功的。下面是一些您可选的解决方法。
<br /><br />

<div class="note">
<table>
<tbody>
<tr>
<td><img src="images/pics/obs.png" alt="obs" /></td>
<td>
必须注意的是，任何程序都是在其原生的平台下表现得最好。不到万不得已不要使用非原生的程序。
</td>
</tr>
</tbody>
</table>
</div><br />



<div class="heading3">12.3.1 Wine</div>
Wine (Wine Is Not an Emulator) 是允许您运行很多 MS Windows 程序的软件，您可以通过 YaST 或者 zypper 安装它。

Wine 是一个命令行程序，可以通过类似这样的命令使用： 
<div class="cl">wine /path/to/setup.exe</div><br />

<div class="tip">
<table>
<tbody>
<tr>
<td><img src="images/pics/tip.png" alt="tip" /></td>
<td>软件包 <a href="http://sourceforge.net/projects/q4wine/" target="_blank">q4wine</a> 
为Wine的一些功能提供了图形界面。
</td>
</tr>
</tbody>
</table>
</div><br />

Wine 项目有一个运行 Windows 程序的经验数据库，可以做些参考：<br />
<a href="http://appdb.winehq.org/appbrowse.php" target="_blank">http://appdb.winehq.org/appbrowse.php</a><br /><br />


<div class="heading3">12.3.2 CrossOver（基于Wine的跨平台兼容工具）</div>
CrossOver软件不是免费的。它可以运行一些精选的主流微软Windows应用程序——多数与办公相关。
<br />
<a href="http://www.codeweavers.com/products/cxlinux/" target="_blank">http://www.codeweavers.com/products/cxlinux/</a><br /><br />


<div class="heading3">12.3.3 双系统启动</div>

正如在 <i>安装</i> 章节中提到的，在同一台电脑上同时安装有 GNU/Linux 和 MS Windows 是非常方便的。如果您仅仅只需要用一些特定的程序，那么可以重启进入 Windows 系统来完成这些特定的工作，至少这对您而言是熟悉的。
<br /><br />

当您安装了双启动系统后，您的 MS Windows 磁盘/分区将被装载到目录 <i>/windows/C/</i>, <i>/windows/D/</i> 等。如果没有，您可以使用 Yast => 系统 => 磁盘分区管理
 来设置挂载点（磁盘分区被挂载到该文件夹里）。默认情况下，NTFS 分区是只读的。要让它们对普通用户可写，您需要编辑 <i>/etc/fstab</i> 中相应的条目，将 <i>fmask=113,dmask=002</i> 替换为 <i>umask=0002</i> 。
<br /><br />

<div class="heading3">12.3.4 虚拟机</div>
在GNU/Linux的 <i>虚拟机</i> 里运行 MS Windows 是可行的，使用比如VirtualBox、
KVM、Xen 或 VMware 之类的软件可以实现。这是高级的东西，
并对电脑的硬件有着更高的要求，毕竟要同时运行两个操作系统。
<br /><br />


