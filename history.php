自由软件并不意味着不花钱（free of charge），
而是自由意义上的自由（but free as in freedom）。
明确的定义按如下四条基本的自由原则：
<br />

<ul>
<li>(0) 可以自由运行该程序，无论是什么目的。
</li>
<li>(1) 可以自由地学习该程序如果工作，并且可以改变它，让它来按您的意愿来执行。
</li>
<li>(2) 
可以自由地重新发布程序的副本，这样您可以帮助您的邻居。
</li>
<li>(3) 
可以自由地改善该程序，公开发布您的改进程序（和常规的版本更新），
这样整个社区都会受益。
</li>
</ul>

自由原则 1 - 3 要求能访问程序的源代码。
<br /><br />


