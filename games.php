<div class="os1">附录B：游戏</div>

没有多少主流游戏可以原生地运行在GNU/Linux上，但还是有大量的游戏选择——至少对于普通玩家。
<br /><br />

<center><img src="images/pics/spil.jpg" alt="spil" class="pic" /></center>


<div class="os2">B.1 原生的GNU/Linux游戏</div><br />
<div class="os3">B.1.1 openSUSE Build Service 游戏软件源</div> 
一些游戏包含在 openSUSE 官方源里，但openSUSE Build Service的 Games 源包含更多的游戏。
 您可以简单地通过社区软件源列表添加这个游戏软件源，添加方法如
  <a href="repositories.php">软件源</a> 章节所述。<br /><br />

或者使用命令行添加游戏源：
<div class="clroot">zypper addrepo -f http://download.opensuse.org/repositories/games/openSUSE_Leap_42.1/ games</div><br />

<div class="os3">B.1.2 Steam 游戏平台和商店</div>
Steam游戏平台已经发布Linux版本，并已经在Linux启用如
《反恐精英》、《反恐精英 起源》、《半条命》、《军团要塞2》、《反恐精英 零点行动》等多款游戏。
售价基本上是10到50美元。<br />
<b>Dota 2已经登陆Linux！安装Steam平台，然后在商店中找到Dota 2装上即可。</b>

<br />
官网：
<a href="http://store.steampowered.com/browse/linux/" target="_blank">http://store.steampowered.com/browse/linux/</a><br />
安装包：
<a href="http://software.opensuse.org/package/steam"  target="_blank">
http://software.opensuse.org/package/steam
</a>

<br /><br />

<div class="os3">B.1.3 Desura</div>
Desura 发行服务为玩家提供到非免费单机游戏的便捷访问：<br />
<a href="http://www.desura.com" target="_blank">http://www.desura.com</a><br /><br />

<div class="os3">B.1.4 其他 GNU/Linux 游戏资源</div>
Linux Game Publishing（Linux游戏发布站）购买主题游戏并将它们移植到
GNU/Linux中，请看：<br />
<a href="http://www.linuxgamepublishing.com" target="_blank">http://www.linuxgamepublishing.com</a><br /><br />

还有很多很多的免费或非免费游戏——一些小而简单，其他的一些很大，还有许多很好的。可从这些网站寻找一些：
<br />

<a href="http://happypenguin.org/" target="_blank">http://happypenguin.org/</a><br />
