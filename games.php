<div class="os2">B.2 运行微软Windows游戏</div>

一些GNU/Linux可用的软件允许您在GNU/Linux上运行为微软Windows开发的
游戏——易用性和成功率可能会有所不同——然而，越受欢迎的游戏，
越有可能被支持。
<br /><br />


<div class="os3">B.2.1 Wine（运行Windows程序的工具）</div>

Wine（Wine Is Not an Emulator，Wine不是个模拟器）是第一选择，
它是开源软件，可以通过软件管理工具（package manager）来安装。
可查看Wine应用程序数据库，获取运行个别游戏的信息：
<br />
<a href="http://appdb.winehq.org/appbrowse.php?iCatId=2" target="_blank">http://appdb.winehq.org/appbrowse.php?iCatId=2</a><br /><br />

Wine是命令行的应用程序，其语法是：
<div class="cl">wine /path/to/setup.exe</div><br />


<div class="os3">B.2.2 PlayOnLinux（基于Wine的跨平台游戏工具）</div>
<a href="http://www.playonlinux.com/" target="_blank">PlayOnLinux</a>
是基于Wine的，可以让您轻松安装和使用（一些）微软Windows游戏。您可以在之前提到的游戏软件源中找到PlayOnLinux的安装包。
<br /><br />

<div class="os3">B.2.3 CrossOver Games（基于Wine的跨平台游戏工具）</div>

第二个选择是CrossOver Games，是基于Wine的跨平台游戏工具，请看：
<br />
<a href="http://www.codeweavers.com/products/cxgames/" target="_blank">http://www.codeweavers.com/products/cxgames/</a><br /><br />


<div class="os2">B.3 模拟器（Emulators，运行经典小游戏）</div>

存在许多模拟器，使在GNU/Linux上运行其他平台的很多老的经典游戏成为可能。
如：
<br />

<ul>
  <li>Amiga (UAE，上世纪80年代的机子，被Commodore收购)</li>
  <li>Arcade (MAME，街机游戏)</li>
  <li>Atari (Stella, Steem，雅达利)</li>
  <li>Commodore 64/128 (VICE，上世纪的PC机)</li>
  <li>MS DOS (DOSBox, DOSEMU，DOS游戏)</li>
  <li>Nintendo (infones, bsnes, nestopia，任天堂电脑游戏)</li>
  <li>Play Station (pcsx, pcsx2，索尼PS2游戏机等)</li>
  
</ul>


