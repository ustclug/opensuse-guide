<a href="http://www.linuxgamingworld.com/" target="_blank">http://www.linuxgamingworld.com/</a><br /><br />

国内所有网页游戏只要按第14章安装Flash插件和Java插件，均可正常游玩:<br />
<a href="http://web.sanguosha.com/" target="_blank">
三国杀
</a><br />

<a href="http://web.4399.com/" target="_blank">
4399网页游戏
</a><br />
<a href="http://www.265g.com/" target="_blank">
265G网页游戏
