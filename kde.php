<div class="heading1">5. KDE 桌面环境</div>

头次启动 openSUSE 后，第一个接触到的便是 KDE Plasma 桌面环境。所谓的桌面环境，包括桌面本身、菜单、面板、文件管理、窗口管理以及一些其他的应用程序。
<br /><br />
KDE Plasma 桌面是高度可配置的，如果有哪些东西您不喜欢，绝大多数情况下您可以按照自己的想法来配置桌面环境。KDE Plasma 桌面包含非常丰富的特性，下面仅是介绍一些最基本的特性。
<br /><br />


<div class="heading2">5.1 桌面</div>
KDE 的桌面与您所熟悉的其他桌面如 Windows 桌面还是有点儿相似的，底部是一个类似任务栏的面板，左侧则有一个类似于开始菜单的东西。<br /><br />

然而有三个地方和 Windows 的桌面是明显不一样的，一定要注意：
<ul>
<li>KDE 下打开项目或者文件默认是 <b>单击</b>。当然也可以调整为双击。</li>
<!--
<li>~/Desktop/ 文件夹下的内容并不是自由的散落在桌面上的，而是放在一个叫做 Folder View (文件夹视图)的桌面小部件里。</li>
-->
<li>默认地，您关机时仍在运行的程序，下次开机启动时会自动打开这些程序。 </i>
</ul>

您可能想看看这个介绍 KDE Plasma 桌面的视频：<br />
<a href="http://v.youku.com/v_show/id_XNTIzNjA2OTIw.html" target="_blank">http://v.youku.com/v_show/id_XNTIzNjA2OTIw.html</a><br /><br /> 

<center><a href="images/screenshots/desktop.png" rel="thumbnail"><img src="images/screenshots/desktopb.png" alt="desktop" class="pic" /></a></center><br />


<div class="heading3">5.1.1 开始菜单</div>
单击屏幕左下角的 K 字母图标可以显示开始菜单（快捷键是 Alt+F1 ），
在开始菜单底部是搜索框，可以搜索应用程序名称，
开始菜单左边有竖排的一列小图标是您喜好的应用程序（侧边栏），可以右击开始菜单里面的应用程序条目，
添加应用到侧边栏或删除侧边栏里的应用。
<br /><br />



<center><a href="images/screenshots/launchmenu.png" rel="thumbnail"><img src="images/screenshots/launchmenub.png" alt="launchmenu" class="pic" /></a></center><br />


您可以自由的编辑菜单，添加和删除应用程序的“快捷方式”： 
<div class="path">右键单击菜单图标 =&gt; 菜单编辑器</div><br />

如果需要将一个快捷方式添加到桌面或者其他面板中，可以这样做（需要小部件在解锁状态）： 
<div class="path">在菜单中找到程序 => 右击该条目 => 单击 "添加到面板" 或者 "添加到桌面"</div><br />


<div class="heading3">5.1.2 虚拟桌面</div>
为避免桌面上开启过多的应用程序窗口导致的措手不及，可以使用虚拟桌面以提高您的工作效率。在底部面板上您能看到一个小小的栅格（2个格子代表2个桌面），这便是虚拟桌面选择器，通过它可以切换虚拟桌面。
<br /><br />


<center><img src="images/screenshots/pager.png" alt="pager" class="pic" /></center><br />


您也可以使用桌面栅格特效看到虚拟桌面的大视图，只需按 <i>Ctrl+F8</i> 
（需要桌面效果支持，请看本章后面桌面效果的段落）。
<br /><br />

<!--
<div class="heading3">5.1.4 活动桌面</div>
靠近虚拟桌面栅格的左边是带三个小圈的立方体，单机它会打开活动桌面管理器。
活动桌面是不同的可定制桌面，可以配置为带有不同小部件、不同壁纸和自动启动的特定应用程序的定制的任务/活动。
<br /><br />

这能让您快速方便地在为不同场景配置的各个桌面（活动）之间切换，例如您可能为工作时配置了一个活动桌面，又为在家时配置了另一个活动桌面，第三个是为您做照片编辑时配置的，第四个是为您做公众演示时配置的，等等，仅需一两次单击，您就可以在这些完全不同的桌面之间切换。屏幕右上角顶部的工具箱显示当前使用的活动桌面名字。
<br /><br />

活动桌面管理器让您能够创建活动桌面和切换它们。另外，通过点击在每个活动桌面上的小覆盖图标进行启动或结束、删除或配置活动，您可以实现各种任务。
<br /><br />


<center><a href="images/screenshots/activities.png" rel="thumbnail"><img src="images/screenshots/activitiesb.png" alt="widgets" class="pic" /></a></center><br />

